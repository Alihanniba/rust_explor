fn main() {
  // 整数相加
  println!("1 + 2 = {}", 1u32 + 2);

  println!("1 - 2 = {}", 1i32 - 2);

  println!("true AND false is {}", true && false);

  println!("true OR false is {}", true || false);

  println!("NOT true is {}", !true);

  // 位运算
  // println!("0011 AND 0101 is {:04b}", )

  println!("One million is written as {}", 1_000_000u32);
}